#include <bits/stdc++.h>
using namespace std;

int main()
{
    int N;
    while(cin>>N)
    {
        if(N==0)
            cout<<"0"<<endl;
        else
        {
            for(int i = 1; i<=N; i++)
            {
                cout<<"a";
            }
            cout<<endl;
        }

    }
    return 0;
}
